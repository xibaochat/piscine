int		ft_any(char **tab, int (*f)(char*))
{
	int i;

	i = 0;
	if (!tab || !f)
		return (0);
	while (tab[i])
	{
		if (((*f)(tab[i])) != 0)
			return (1);
		i++;
	}
	return (0);
}
