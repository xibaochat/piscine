int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	if (!tab || !*tab || !length || !f)
		return (0);
	while (tab[i])
	{
		if ((*f)(tab[i]))
			c++;
		i++;
	}
	return (c);
}
